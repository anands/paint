def gen_pattern(char):
	global date
	for i in range(35):
		check = patterns[char][i]
		if check:
			DATES.append(date)
		date += datetime.timedelta(days=1)

def gen_empty_pattern():
	global date
	for i in range(7):
		date += datetime.timedelta(days=1)

def init(message="GITHUB", author_name="", author_email=""):

	class author:
		name = author_name
		email = author_email

	_author = author()

	message = message.upper()
	string = message[0:8]
	for char in string:
		gen_pattern(char)
		gen_empty_pattern()
	
	random_dir = str(uuid.uuid4())
	dir_path = os.getcwd() + "/gits/" + random_dir

	import github
	REPO = github.init(dir_path)

	for date in DATES:
		date = date.strftime("%Y-%m-%d %H:%M:%S")
		github.commit(REPO, date, dir_path, _author)

	return "./gits/" + random_dir